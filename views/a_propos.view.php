                    L’objectif de l’action locale de GdA est d’apporter une réponse à des questions locales en appliquant les propositions de GdA sur le terrain. Les actions locales de GdA auront deux formes: d’une part les manifestations organisées dans le cadre de la préparation des projets (conférences, débats, séances de travail en commun) et d’autre part la mise en pratique des projets aillant aboutis.</b></div>





            <div class="tab-pane fade text-propos-card mt-4 ml-4 mr-4 pr-4 pl-4" id="list-profile" role="tabpanel" aria-labelledby="list-profile-list"><b>La création d’un réseau de membres actifs à l’échelle nationale est le premier objectif de GdA.<br>

                    La diversité des références, des idées, des styles de vie et des centres d’intérêt des jeunes apporte une véritable richesse culturelle et intellectuelle à la société. Nous voulons représenter cette jeunesse, dans toute sa diversité. Nos propositions ne pourront être recevables que si elles émanent d’un large spectre de jeunes, unis par leur volonté d’agir.<br>

                    La première démarche visant à mettre ce réseau en place se fera au niveau national et visera les universités franciliennes, les principales écoles de commerce post-bac et les IEP (aussi bien le campus central de Paris que les campus décentralisés et les IEP du concours commun, ainsi que Grenoble et Bordeaux). Nous souhaitons rassembler des personnes de tous horizons afin de générer des idées riches, et des dialogues de plus en plus constructifs. En effet, c’est la pluralité des expériences et des opinions qui feront la richesse de GdA et la pertinence de nos actions.<br>

                    Nous souhaitons dans un second temps nous développer dans les lycées. En ouvrant une porte vers l’investissement direct dans des projets locaux et nationaux concrets et en fournissant un soutien matériel et institutionnalisé, nous voulons non seulement répondre à une demande de la jeunesse mais également développer une organisation capable de sans cesse nous renouveler et d’atteindre des individus aux profils extrêmement divers et riches. Cela dans le but d’impliquer les lycéens désireux d’agir dans un processus constructif au sein même de leur lieu de vie et d’étude.<br>

                    La base de ce projet repose sur réseau de soutiens fort et actif. Le mode de fonctionnement de GdA nécessite le soutien en particulier de parlementaires, sénateurs et députés, qui pourront porter les propositions et rapports mis en forme par les membres au sein du Parlement.<br>

                    Contrairement à d’autres Think-Tanks et associations poursuivant les mêmes objectifs que nous, nous ne voulons pas seulement publier des rapports et propositions sans suites. Nous voulons les porter directement aux institutions, avec un réel soutien parlementaire et ou politique. Nous avons déjà commencé à établir des connections avec plusieurs parlementaires, cependant nous souhaitons un réseau parlementaire beaucoup plus dense afin d’élargir notre marge de manoeuvre et de proposition. Il est donc important de continuer à développer ce réseau national et surtout de mettre en place un réseau local d’élus et d’associations partenaires, qui servira de base de soutien et de consultation lors de la réalisation de projets mais qui pourra également servir à l’organisation d’évènements telles que des conférences et des débats animés par des personnalités reconnues dans les domaines d’action de GdA.<br>

                    Ainsi le premier objectif est la création d’un réseau de personnalités variées, qui pourront œuvrer pour la réalisation des projets de GdA de diverses manières, soit en finançant l’association, soit en participant à la réflexion préparatoire, soit en portant nos rapports et idées au sein des hémicycles du Parlement.</b></div>
